package org.example;

/**
 * Краткое описание в одном-двух предложениях.
 * Более подробное описание при необходимости.
 *
 * @author  Ефим
 * @since   1.0
 */
public class Example {
    /**
     * Возвращает сумму двух чисел.
     *
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @return сумма a и b
     * @throws IllegalArgumentException если a или b некорректны
     */
    public int sum(int a, int b) {
        return a + b;
    }
}
